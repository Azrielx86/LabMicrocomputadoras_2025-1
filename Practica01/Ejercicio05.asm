PROCESSOR 16F877
INCLUDE <P16F877.inc>

AX equ 0x26
COUNTR equ 0x27
   ORG 0 
   GOTO INICIO 
   ORG 5

INICIO: 
   BCF STATUS, 2
   MOVLW 0X00
   MOVWF AX 
   MOVLW 0X09; Carga en 10
   MOVWF COUNTR

LOOP:
    BTFSC STATUS, 2
    GOTO RESETCNT
    INCF AX
    DECF COUNTR
    GOTO LOOP

RESETCNT:
    MOVLW 0x07
    ADDWF AX
    MOVF AX, W 
    XORLW 0x20
    BTFSC STATUS, 2
    GOTO INICIO
    MOVLW 0x09;
    MOVWF COUNTR
    BCF STATUS, 2
    GOTO LOOP
END
