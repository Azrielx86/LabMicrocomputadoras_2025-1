	PROCESSOR 16f877A
	INCLUDE <p16f877A.inc> 
AX	EQU 0x40
	ORG 0
	GOTO INICIO 
	ORG 5
INICIO:
	BCF		STATUS,7
	BCF 	STATUS,RP1 ; Para seleccionar el primer banco
	BCF 	STATUS,RP0
	MOVLW 	0X20
	MOVWF 	FSR
	MOVF 	INDF,W
	MOVWF	AX
	INCF 	FSR
LOOP:
	MOVF	INDF,W
	SUBWF	AX,W
	
	BTFSS	STATUS,0
	GOTO 	MENOR
	MOVF	INDF,W
	MOVWF	AX
MENOR:
	INCF 	FSR
	BTFSS 	FSR,6
	GOTO 	LOOP
	GOTO 	$
	END